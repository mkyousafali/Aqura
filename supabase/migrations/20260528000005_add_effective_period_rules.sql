-- Add multi-period applicability rule schedules with effective from/to and infinite support

CREATE TABLE IF NOT EXISTS public.hr_employee_applicability_rule_periods (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id text NOT NULL,
    rule_type text NOT NULL,
    rule_id bigint NOT NULL,
    sequence_no integer NOT NULL,
    duration_years integer NOT NULL DEFAULT 0,
    duration_months integer NOT NULL DEFAULT 0,
    effective_from date NOT NULL,
    effective_to date,
    is_infinite boolean NOT NULL DEFAULT false,
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    updated_at timestamp with time zone NOT NULL DEFAULT now(),
    CONSTRAINT hr_employee_applicability_rule_periods_employee_fkey
        FOREIGN KEY (employee_id) REFERENCES public.hr_employee_master(id) ON DELETE CASCADE,
    CONSTRAINT hr_employee_applicability_rule_periods_rule_fkey
        FOREIGN KEY (rule_id) REFERENCES public.settlement_rules(id) ON DELETE CASCADE,
    CONSTRAINT hr_employee_applicability_rule_periods_type_check
        CHECK (rule_type IN ('ticket', 'leave_salary')),
    CONSTRAINT hr_employee_applicability_rule_periods_duration_check
        CHECK (
            (is_infinite = true AND effective_to IS NULL)
            OR (is_infinite = false AND effective_to IS NOT NULL AND effective_to >= effective_from)
        ),
    CONSTRAINT hr_employee_applicability_rule_periods_unique_seq
        UNIQUE (employee_id, rule_type, sequence_no)
);

CREATE INDEX IF NOT EXISTS hr_employee_applicability_rule_periods_employee_type_idx
    ON public.hr_employee_applicability_rule_periods (employee_id, rule_type);

CREATE INDEX IF NOT EXISTS hr_employee_applicability_rule_periods_effective_idx
    ON public.hr_employee_applicability_rule_periods (employee_id, rule_type, effective_from, effective_to);

CREATE OR REPLACE FUNCTION public.update_hr_employee_applicability_rule_periods_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS set_hr_employee_applicability_rule_periods_updated_at
    ON public.hr_employee_applicability_rule_periods;

CREATE TRIGGER set_hr_employee_applicability_rule_periods_updated_at
BEFORE UPDATE ON public.hr_employee_applicability_rule_periods
FOR EACH ROW
EXECUTE FUNCTION public.update_hr_employee_applicability_rule_periods_updated_at();

ALTER TABLE public.hr_employee_applicability_rule_periods ENABLE ROW LEVEL SECURITY;

