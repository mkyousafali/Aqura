-- Track consumed qualified tickets and leave days, and expose current-period usage via RPC

CREATE TABLE IF NOT EXISTS public.hr_employee_ticket_issuances (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id text NOT NULL,
    issuance_date date NOT NULL,
    ticket_count integer NOT NULL CHECK (ticket_count > 0),
    ticket_amount numeric(12,2) NOT NULL DEFAULT 0 CHECK (ticket_amount >= 0),
    is_paid boolean NOT NULL DEFAULT false,
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    updated_at timestamp with time zone NOT NULL DEFAULT now(),
    CONSTRAINT hr_employee_ticket_issuances_employee_fkey
        FOREIGN KEY (employee_id) REFERENCES public.hr_employee_master(id) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS hr_employee_ticket_issuances_employee_date_idx
    ON public.hr_employee_ticket_issuances (employee_id, issuance_date);

CREATE TABLE IF NOT EXISTS public.hr_employee_leave_approvals (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id text NOT NULL,
    leave_date date NOT NULL,
    is_paid boolean NOT NULL DEFAULT false,
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    updated_at timestamp with time zone NOT NULL DEFAULT now(),
    CONSTRAINT hr_employee_leave_approvals_employee_fkey
        FOREIGN KEY (employee_id) REFERENCES public.hr_employee_master(id) ON DELETE CASCADE,
    CONSTRAINT hr_employee_leave_approvals_employee_date_key UNIQUE (employee_id, leave_date)
);

CREATE INDEX IF NOT EXISTS hr_employee_leave_approvals_employee_date_idx
    ON public.hr_employee_leave_approvals (employee_id, leave_date);

CREATE OR REPLACE FUNCTION public.update_hr_employee_ticket_issuances_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS set_hr_employee_ticket_issuances_updated_at
    ON public.hr_employee_ticket_issuances;
CREATE TRIGGER set_hr_employee_ticket_issuances_updated_at
BEFORE UPDATE ON public.hr_employee_ticket_issuances
FOR EACH ROW
EXECUTE FUNCTION public.update_hr_employee_ticket_issuances_updated_at();

CREATE OR REPLACE FUNCTION public.update_hr_employee_leave_approvals_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
