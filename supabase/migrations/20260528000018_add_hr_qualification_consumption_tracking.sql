CREATE TRIGGER set_hr_employee_leave_approvals_updated_at
BEFORE UPDATE ON public.hr_employee_leave_approvals
FOR EACH ROW
EXECUTE FUNCTION public.update_hr_employee_leave_approvals_updated_at();

ALTER TABLE public.hr_employee_ticket_issuances ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.hr_employee_leave_approvals ENABLE ROW LEVEL SECURITY;

